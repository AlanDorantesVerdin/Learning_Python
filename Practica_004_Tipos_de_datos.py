# 1. Tipo de dato cadena de texto (str)
print("1. CADENAS DE TEXTO")
TEXTO_1 = "Hola, soy Alan"
print(TEXTO_1)
print(type(TEXTO_1)) 
print()

# 2. Tipo de dato cadena de texto (str) con comillas simples
print("2. CADENAS DE TEXTO CON COMILLAS SIMPLES")
TEXTO_1 = 'Hola, soy Alan'
print(TEXTO_1)
print(type(TEXTO_1))
print()

# 3. Tipo de dato entero (int)
print("3. NÚMEROS ENTEROS")
NUMERO_1 = 10
print(NUMERO_1)
print(type(NUMERO_1))
print()

# 4. Tipo de dato entero (int) negativo
print("4. NÚMEROS ENTEROS NEGATIVOS")
NUMERO_1 = -10
print(NUMERO_1)
print(type(NUMERO_1)) 
print()

# 5. Tipo de dato flotante (float)
print("5. NÚMEROS DECIMALES")
NUMERO_2 = 3.14159
print(NUMERO_2)
print(type(NUMERO_2)) 
print()

# 6. Tipo de dato flotante (float) negativo
print("6. NÚMEROS DECIMALES NEGATIVOS")
NUMERO_2 = -3.14159
print(NUMERO_2)
print(type(NUMERO_2))
print()

# 7. Tipo de dato booleano (bool)
print("7. VALORES BOOLEANOS")
BOOLEANO_1 = True
print(BOOLEANO_1)
print(type(BOOLEANO_1))
print()
BOOLEANO_1 = False
print(BOOLEANO_1)  
print(type(BOOLEANO_1))
print()

# 8. Tipo de dato lista (list)
print("8. LISTAS")
LISTA_1 = [1, 2, 3, 4, 5]
print(LISTA_1)
print(type(LISTA_1))
print()

# 9. Conversión entre tipos de datos 
print("9. CONVERSIÓN ENTRE TIPOS DE DATOS")
NUMERO_3 = "100" # Cadena de texto que representa un número
NUMERO_3 = int(NUMERO_3)  # Convertir cadena a entero
print(NUMERO_3)
print(type(NUMERO_3))  
