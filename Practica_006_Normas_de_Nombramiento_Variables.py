# No todas las combinaciones de caracteres son válidas para nombres de variables:

# Nombres de variables válidos
numero1 = 1
fecha_actual = "2024-10-05"
resultado = 0

# Nombres de variables inválidos (descomentar para ver los errores)
# 1numero (no puede comenzar con un número)
# $resultado (no puede comenzar con un símbolo)
# nombre-usuario (no puede contener guiones)

# Buenas prácticas para nombrar variables: 

# 1. Usar nombres descriptivos y significativos
saludo = "Hola"
numero = 7
numero_preciso = 7.5657

# 2. Usar minúsculas y guiones bajos para separar palabras (snake_case)
nombre_completo = "Alan Dorantes"
hora_actual = "10:30"

# 3. No usar mayusculas a excepción de constantes
PI = 3.14159
GRAVEDAD = 9.81

# 4. Evitar acentos y caracteres especiales
numero = 5
番号 = 10  

# 5. Escribir en inglés si es posible (en especial para proyectos internacionales)
user_age = 25
user_name = "Alan"
